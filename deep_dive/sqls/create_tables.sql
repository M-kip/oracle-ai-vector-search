CREATE TABLE IF NOT EXISTS C##MOSES.my_books (
    id INTEGER GENERATED BY DEFAULT ON NULL AS IDENTITY
    (START WITH 1 CACHE 20 ) PRIMARY KEY,
    file_name VARCHAR2 (900),
    file_size INTEGER,
    file_type VARCHAR2 (100),
    file_content BLOB
) LOGGING;

CREATE TABLE IF NOT EXISTS C##MOSES.vector_store (
    "DOC_ID" NUMBER(*, 0) NOT NULL ENABLE,
    "EMBED_ID" NUMBER,
    "EMBED_DATA" VARCHAR2(4000 BYTE),
    "EMBED_VECTOR" VECTOR,
        FOREIGN KEY (DOC_ID) REFERENCES
        my_books(id)
);